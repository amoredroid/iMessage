class Operations {
    double actAdd(double firstNum, double secondNum) {
        return firstNum + secondNum;
    }

    double actSub(double firstNum, double secondNum) {
        return firstNum - secondNum;
    }

    double actMult(double firstNum, double secondNum) {
        return firstNum * secondNum;
    }

    double actDiv(double firstNum, double secondNum) {
        return firstNum / secondNum;
    }
}
