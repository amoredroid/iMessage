public class Operations {
    public double actAdd(double firstNum, double secondNum) {
        return firstNum + secondNum;
    }

    public double actSub(double firstNum, double secondNum) {
        return firstNum - secondNum;
    }

    public double actMult(double firstNum, double secondNum) {
        return firstNum * secondNum;
    }

    public double actDiv(double firstNum, double secondNum) {
        return firstNum / secondNum;
    }
}
